"""Synthetic relational training data, composed deterministically from real
call-graph facts -- not model-generated. Concretely: if A calls B and B
calls C (both real, AST-verified facts), explicitly state "A transitively
depends on C" as a synthetic training example. This is a training-time
mechanism, distinct from graph.py's TokenGraph (corpus-level, consulted at
INFERENCE time via one-hop lookup) -- the goal here is for the model's own
parameters to learn the compositional pattern via gradient descent on real
examples, not to add another inference-time lookup crutch.

Real precedent, not a novel untested idea: CLUTRR (Sinha et al. 2019,
arXiv:1908.06177) trains/tests relational composition on exactly this
shape of synthetically-composed-from-real-facts data. The critical design
constraint, and the reason this doesn't risk model collapse (Shumailov
et al. 2024, Nature -- recursively training on a model's own generations
degrades it): every synthetic statement is a deterministic composition of
facts already verified real by ast.parse, never text generated by Ducky
itself. Several template phrasings per chain, not one fixed mold, so
training can't just pattern-match a single surface form instead of the
underlying relation.

Narrow by design, same discipline as check_call_arity_consistency: only
direct calls to simple Name-bound functions are tracked (not
attribute-method calls, which would need real type inference to resolve).
"""
import ast
import builtins
import random
import re

_BUILTIN_NAMES = frozenset(dir(builtins))

TEMPLATES = [
    "# {a} calls {b}, which calls {c} -- so {a} transitively depends on {c}.",
    "# Since {a} invokes {b} and {b} invokes {c}, {a} indirectly relies on {c}.",
    "# {a} -> {b} -> {c}: {a} has a transitive dependency on {c} through {b}.",
    "# Note: {a} does not call {c} directly, but reaches it via {b}.",
]

ONE_HOP_TEMPLATES = [
    "# {a} calls {b}.",
    "# {a} depends directly on {b}.",
]


def build_call_graph(code_source: str) -> dict:
    """Whole-function-name call graph: caller -> {callees}, only direct
    calls to simple `name(...)` targets (not `obj.method(...)`, same
    narrow scope as check_call_arity_consistency -- resolving which
    method a `.` call targets needs real type inference).
    """
    tree = ast.parse(code_source)
    call_graph: dict = {}
    for node in ast.walk(tree):
        if not isinstance(node, ast.FunctionDef):
            continue
        callees = set()
        for inner in ast.walk(node):
            if isinstance(inner, ast.Call) and isinstance(inner.func, ast.Name):
                if inner.func.id != node.name:  # skip direct self-recursion, not a chain
                    callees.add(inner.func.id)
        if callees:
            call_graph.setdefault(node.name, set()).update(callees)
    return call_graph


def find_transitive_chains(call_graph: dict, max_chains: int = None) -> list:
    """Real, verifiable (A, B, C) triples where A->B and B->C both exist
    as real edges in call_graph, and A != C (excludes trivial round-trips).
    Deterministic graph traversal, not generation -- every chain returned
    is composed entirely from edges that were themselves AST-verified.
    """
    chains = []
    for a, b_set in call_graph.items():
        for b in b_set:
            for c in call_graph.get(b, ()):
                if c != a:
                    chains.append((a, b, c))
    if max_chains is not None and len(chains) > max_chains:
        chains = random.sample(chains, max_chains)
    return chains


def filter_generic_chains(chains: list) -> list:
    """Drops chains whose bridge or endpoint is a Python builtin (open,
    len, isinstance, str, ...) -- measured, not assumed: on the stdlib
    call graph, 'open' alone is the bridge in 20.5% of all chains, and
    43.8%/46.3% have a builtin bridge/endpoint respectively. "X calls
    open, which is called by everything" carries almost no distinguishing
    signal about X specifically, unlike a chain through a real,
    domain-specific function. Roughly halves the chain count.
    """
    return [(a, b, c) for a, b, c in chains if b not in _BUILTIN_NAMES and c not in _BUILTIN_NAMES]


def synthesize_examples(chains: list, seed: int = 0) -> list:
    """One templated synthetic statement per chain, template chosen
    pseudo-randomly (seeded for reproducibility) so repeated runs on the
    same chains produce the same training text, not a moving target.
    """
    rng = random.Random(seed)
    examples = []
    for a, b, c in chains:
        template = rng.choice(TEMPLATES)
        examples.append(template.format(a=a, b=b, c=c))
    return examples


def extract_mentioned_functions(text: str, call_graph: dict) -> list:
    """Which known (whole-identifier) functions are actually present in
    this text, as whole words -- not a substring match, so a mention of
    "meaning" doesn't falsely match a function called "mean".
    """
    mentioned = []
    for name in call_graph:
        if re.search(rf"\b{re.escape(name)}\b", text):
            mentioned.append(name)
    return mentioned


def retrieve_relevant_facts(mentioned: list, call_graph: dict, max_facts: int = 3, seed: int = 0) -> list:
    """Real facts about the mentioned functions, formatted with the SAME
    template family used for training-time synthetic data (synthesize_examples)
    -- deliberately reusing the identical text shape the model may have
    been trained to read, rather than inventing a new format at inference
    time. Prefers 2-hop chains (more informative) over bare 1-hop facts,
    per mentioned function, capped at max_facts total.
    """
    rng = random.Random(seed)
    facts = []
    for name in mentioned:
        if len(facts) >= max_facts:
            break
        callees = call_graph.get(name, set())
        two_hop = [(name, b, c) for b in callees for c in call_graph.get(b, ()) if c != name]
        if two_hop:
            a, b, c = rng.choice(two_hop)
            facts.append(rng.choice(TEMPLATES).format(a=a, b=b, c=c))
        elif callees:
            b = rng.choice(sorted(callees))
            facts.append(rng.choice(ONE_HOP_TEMPLATES).format(a=name, b=b))
    return facts[:max_facts]


def inject_context(prompt: str, call_graph: dict, max_facts: int = 3, seed: int = 0) -> str:
    """Retrieval-augmented prompt construction: pulls real graph facts
    relevant to what's already mentioned in the prompt and prepends them
    as text -- not blended into logits (that's graph.py/inference.py's
    older, weaker mechanism). If nothing in the prompt matches a known
    function, returns the prompt unchanged rather than injecting noise.
    """
    mentioned = extract_mentioned_functions(prompt, call_graph)
    facts = retrieve_relevant_facts(mentioned, call_graph, max_facts=max_facts, seed=seed)
    if not facts:
        return prompt
    preamble = "\n".join(facts)
    return f"{preamble}\n{prompt}"


if __name__ == "__main__":
    from pathlib import Path

    ROOT = Path(__file__).resolve().parent.parent
    code_source = (ROOT / "data" / "code" / "corpus.txt").read_text()

    call_graph = build_call_graph(code_source)
    n_edges = sum(len(v) for v in call_graph.values())
    print(f"call graph: {len(call_graph)} functions with outgoing calls, {n_edges} direct-call edges")

    chains = find_transitive_chains(call_graph)
    print(f"found {len(chains)} real 2-hop transitive chains (A calls B calls C, A != C)")

    examples = synthesize_examples(chains, seed=0)
    print(f"\nsample synthetic examples (first 10 of {len(examples)}):")
    for ex in examples[:10]:
        print(f"  {ex}")
